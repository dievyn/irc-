#include "libft.h"
#include <unistd.h>
#include <stdio.h>
#include <stdlib.h>

int	free_tab(char **tab)
{
	int	i;

	i = 0;
	while (tab[i])
	{
		free(tab[i]);
		i++;
	}
	if (tab)
		free(tab);
	return (1);
}
